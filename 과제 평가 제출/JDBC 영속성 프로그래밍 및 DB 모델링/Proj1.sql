-- 음료 테이블 생성
CREATE TABLE DRINKS (
    DRINKNO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DRINKNAME VARCHAR2(30) NOT NULL,
    CATEGORY VARCHAR2(10) CHECK (CATEGORY IN ('커피', '차')) NOT NULL,
    PRICE INT NOT NULL
);

-- 관리자 테이블 생성
CREATE TABLE ADMIN (
    ID VARCHAR2(20) PRIMARY KEY,
    PASSWORD VARCHAR2(20) NOT NULL
);

